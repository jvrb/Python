#8)  Faça agora o contrário, de Fahrenheit para Celsius.

print("Programa para converter Celsius para Fahrenheit")
fahrenheit = float(input("Digite o valor em Graus Fahrenheit: "))
celsius = (fahrenheit - 32) / 1.8
print("{} Graus Fahrenheit convertido para Fahrenheit é igual a {} C" .format(fahrenheit,celsius))
